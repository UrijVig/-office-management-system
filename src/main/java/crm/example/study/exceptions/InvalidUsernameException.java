package crm.example.study.exceptions;

/**
 * Исключение, выбрасываемое при попытке использования некорректного имени
 * пользователя.
 * Используется для валидации имен пользователей, которые не соответствуют
 * требованиям системы,
 * таким как уникальность, формат или другие бизнес-правила.
 */
public class InvalidUsernameException extends Exception {

    /**
     * Создает новое исключение с указанным сообщением об ошибке.
     * 
     * @param message детальное сообщение об ошибке, описывающее причину исключения
     */
    public InvalidUsernameException(String message) {
        super(message);
    }
}
